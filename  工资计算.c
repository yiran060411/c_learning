#include <stdio.h>

int main() {
    int T;
    scanf("%d", &T);

    for (int S = 3500; ; S += 100) { 
        int A = S - 3500;
        int tax = 0;

        if (A > 0) {
            if (A <= 1500) {
                tax = A * 0.03;
            } else if (A <= 4500) {
                tax = 1500 * 0.03 + (A - 1500) * 0.10;
            } else if (A <= 9000) {
                tax = 1500 * 0.03 + 3000 * 0.10 + (A - 4500) * 0.20;
            } else if (A <= 35000) {
                tax = 1500 * 0.03 + 3000 * 0.10 + 4500 * 0.20 + (A - 9000) * 0.25;
            } else if (A <= 55000) {
                tax = 1500 * 0.03 + 3000 * 0.10 + 4500 * 0.20 + 26000 * 0.25 + (A - 35000) * 0.30;
            } else if (A <= 80000) {
                tax = 1500 * 0.03 + 3000 * 0.10 + 4500 * 0.20 + 26000 * 0.25 + 20000 * 0.30 + (A - 55000) * 0.35;
            } else {
                tax = 1500 * 0.03 + 3000 * 0.10 + 4500 * 0.20 + 26000 * 0.25 + 20000 * 0.30 + 25000 * 0.35 + (A - 80000) * 0.45;
            }
        }

        if (S - tax == T) { 
            printf("%d\n", S);
            break;
        }
    }

    return 0;
}